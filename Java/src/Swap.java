
public class Swap {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("Add logic");
		a=a+b;
		b=a-b;
		a=a-b;
		

		System.out.println("A:" +a);
		System.out.println("B:" +b);
		
		System.out.println("Div logic");
		a=a*b;
		b=a/b;
		a=a/b;
		

		System.out.println("A:" +a);
		System.out.println("B:" +b);
		
		System.out.println("XOR logic");
		a=a^b;
		b=a^b;
		a=a^b;
		

		System.out.println("A:" +a);
		System.out.println("B:" +b);


	}

}
